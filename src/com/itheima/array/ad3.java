package com.itheima.array;

public class ad3 {

    public static void main(String[] args) {

        /*
            数组的遍历
            定义一个整数数组,里面存储任意数据,
            并将数组遍历并打印。
        */

        int[] arr = {10,20,30,40,50};
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}
