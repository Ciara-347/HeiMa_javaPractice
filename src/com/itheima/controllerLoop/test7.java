package com.itheima.controllerLoop;

public class test7 {

    public static void main(String[] args) {

        /*
            制表符:\t
            简单理解:长度可变的大空格,打印表格类数据的时候,
            可以让上下对其。(99乘法表)

            真正的含义:
            在前面的字符后面补1-4个空格,
            让这个整体的长度凑成4的整数倍----idea
            在前面的字符后面补1-8个空格,
            让这个整体的长度凑成8的整数倍


            姓名      年龄      性别
            张三      18       男
            李四      19       女
        */

        System.out.println("姓名\t\t年龄\t\t性别");
        System.out.println("张三\t\t18\t\t男");
        System.out.println("李四\t\t19\t\t女");

        System.out.println("--------");
        System.out.println("----\t--");

    }
}
