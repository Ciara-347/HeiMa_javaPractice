package com.itheima.loopdowhile;

public class dwd1 {

    public static void main(String[] args) {

        /*
            利用do ... while循环,输出5行"hello world"
            注:do ... while循环,熟悉语法即可,无需额外练习
        */

        int i = 1;
        do{
            System.out.println("hello world");
            i++;
        }while(i <= 0);
    }
}
