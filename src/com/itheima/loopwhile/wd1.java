package com.itheima.loopwhile;

public class wd1 {

    public static void main(String[] args) {

        /*
            练习9:循环重复跳跃
            利用while循环,实现游戏中连续跳跃10次,用输出语句模拟跳跃的逻辑
        */

        int i = 1;
        while(i <= 10){
            System.out.println("跳跃");
            i++;
        }
    }
}
